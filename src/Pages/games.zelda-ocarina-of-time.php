<h1 id="intro">Introduction <a href="#intro">#</a></h1>
<p>
    I was quite young when Ocarina of Time came out. To be honest I don't even really remember my first experience of the game.
    My mom played through it while I watched over many many sessions. She always mentions "That damn shadow temple!"
    when we talk about those times. I wish I had a photo or video of one of those nights.
</p>

<p>
    Some time before the release of <a href="/games/zelda-majoras-mask">majora's mask</a> I have slightly more vivid memories
    of &quot;playing&quot; through the game with my baby sitter Carolyn. I use the term playing loosely because it was
    mostly Carolyn playing while I watched. She would hand the controller over to me whenever we got to a big chest so
    I could enthusiastically hit the <strong style="color: #3C5AA6;">A</strong> button.
</p>

<p>
    I have fragmented memories of the afternoons at Carolyn's house. The smells of Pizza Hut, Mandarin Cranberry candle, and I'm pretty sure she had a dog.
    Not to get too far off topic. It's just something that comes to mind when I think about Ocarina of Time.
</p>

<p>
    I have a few more random memories of playing the original game that come to mind:
</p>

<ul>
    <li>Riding Epona around in hyrule field. My grandparents were watching and I remember them laughing and yelling, &quot;smack her butt&quot; when I'd feed Epona a carrot</li>
    <li>Playing on a weird CRT at my Aunt Deb's that had a weird ghosting artifact</li>
    <li>Playing over RF on a crappy CRT one weekend when I was visiting my Dad</li>
</ul>

<p>
    I'm not actual sure when I completed my own first playthrough of the game.
    I remember doing a playthrough on a really crappy PC running <a href="https://www.pj64-emu.com/">PJ64</a> some time around 2005 when
    my Dad and Step Mom were living in the farm house. I didn't have a USB controller or adapater so I actually played through
    a ton of the game on keyboard.
</p>

